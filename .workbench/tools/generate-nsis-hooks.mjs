const encodedCustomKnowledgeCleanup = Buffer.from(
  customKnowledgeCleanupScript,
  "utf16le",
).toString("base64");
const cmdkeyLines = credentialTargets
  .map(
    (target) =>
      `    nsExec::ExecToLog '"$SYSDIR\\cmdkey.exe" /delete:${target}'\n    Pop $0`,
  )
  .join("\n");

const nsis = `; Generated by .workbench/tools/generate-nsis-hooks.mjs
!macro NSIS_HOOK_POSTUNINSTALL
  \${If} $DeleteAppDataCheckboxState = 1
  \${AndIf} $UpdateMode <> 1
    SetShellVarContext current
    nsExec::ExecToLog '"$SYSDIR\\WindowsPowerShell\\v1.0\\powershell.exe" -NoProfile -NonInteractive -WindowStyle Hidden -ExecutionPolicy Bypass -EncodedCommand ${encodedCustomKnowledgeCleanup}'
    Pop $0
    RMDir /r "$APPDATA\\Wridian"
    RMDir /r "$LOCALAPPDATA\\Wridian"
    RMDir /r "$APPDATA\\ai.wridian.app"
    RMDir /r "$LOCALAPPDATA\\ai.wridian.app"
${cmdkeyLines}
  \${EndIf}
!macroend
`;

for (const forbidden of [
  'RMDir /r "D:\\Wridian知识库"',
  'RMDir /r "$PROFILE\\Wridian知识库"',
  'RMDir /r "$DOCUMENTS\\Wridian知识库"',
]) {
  if (nsis.includes(forbidden)) {
    throw new Error(`Unsafe whole knowledge-root deletion generated: ${forbidden}`);
  }
}

writeFileSync(new URL("../../src-tauri/nsis-hooks.nsh", import.meta.url), nsis);
