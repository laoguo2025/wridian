import { writeFileSync } from "node:fs";

const customKnowledgeCleanupScript = String.raw`$ErrorActionPreference='SilentlyContinue'
$cfg=Join-Path $env:APPDATA 'Wridian\.wridian\workspace.json'
if(Test-Path -LiteralPath $cfg){try{$root=[string]((Get-Content -Raw -LiteralPath $cfg|ConvertFrom-Json).knowledgeRoot)}catch{}}
if($root){
  $docs=[Environment]::GetFolderPath('MyDocuments')
  $defaults=@('D:\Wridian知识库',(Join-Path $env:USERPROFILE 'Wridian知识库'))
  if($docs){$defaults+=(Join-Path $docs 'Wridian知识库')}
  function N([string]$p){try{[IO.Path]::GetFullPath($p).TrimEnd('\')}catch{$p.TrimEnd('\')}}
  if(($defaults|ForEach-Object{N $_}) -notcontains (N $root)){
    Remove-Item -LiteralPath (Join-Path $root '.wridian'),(Join-Path $root '.wridian-trash'),(Join-Path $root 'hot.md'),(Join-Path $root '00知识库治理\folds') -Recurse -Force
    $g=Join-Path $root '00知识库治理'
    if(Test-Path -LiteralPath $g){Get-ChildItem -LiteralPath $g -Filter '知识库体检-*.md' -File|Remove-Item -Force}
  }
}
`;

