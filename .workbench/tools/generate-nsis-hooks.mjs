import { writeFileSync } from "node:fs";

const customKnowledgeCleanupScript = String.raw`$ErrorActionPreference='SilentlyContinue'
$cfg=Join-Path $env:APPDATA 'Wridian\.wridian\workspace.json'
if(Test-Path -LiteralPath $cfg){try{$root=[string]((Get-Content -Raw -LiteralPath $cfg|ConvertFrom-Json).knowledgeRoot)}catch{}}
function Clear-WridianKnowledgeArtifacts([string]$root){
  if([string]::IsNullOrWhiteSpace($root)){return}
  $resolved=$null
  try{$resolved=[IO.Path]::GetFullPath($root).TrimEnd('\')}catch{return}
  if(-not (Test-Path -LiteralPath $resolved -PathType Container)){return}
  Remove-Item -LiteralPath (Join-Path $resolved '.wridian'),(Join-Path $resolved '.wridian-trash'),(Join-Path $resolved 'hot.md'),(Join-Path $resolved '00知识库治理\folds') -Recurse -Force
  $g=Join-Path $resolved '00知识库治理'
  if(Test-Path -LiteralPath $g){Get-ChildItem -LiteralPath $g -Filter '知识库体检-*.md' -File|Remove-Item -Force}
}
$docs=[Environment]::GetFolderPath('MyDocuments')
$roots=@('D:\Wridian知识库',(Join-Path $env:USERPROFILE 'Wridian知识库'))
if($docs){$roots+=(Join-Path $docs 'Wridian知识库')}
if($root){$roots+=$root}
$seen=@{}
foreach($candidate in $roots){
  try{$key=[IO.Path]::GetFullPath($candidate).TrimEnd('\').ToLowerInvariant()}catch{continue}
  if(-not $seen.ContainsKey($key)){
    $seen[$key]=$true
    Clear-WridianKnowledgeArtifacts $candidate
  }
}
`;

